# Condicionales

valor1 <- 10
valor2 <- 40

if (valor1 == valor2) {
  print("Son iguales")
} else {
  print("Son diferentes")
}


if(valor1 > valor2) {
  paste(valor1 ,"es mayor que ", valor2 )
} 

if (valor1 < valor2) {
  paste(valor1 ,"es menor que ", valor2 )
}

if (valor1 == valor2) {
  paste(valor1 ,"es igual que ", valor2 )
}
